                <h1>Convening cross-sector stakeholders.</h1>
                <p class="">Dementia Network Calgary is a growing group of knowledgeable, capable, and passionate
                    individuals from across public, private and nonprofit sectors in Calgary and area with an interest
                    in Alzheimer's disease and related dementias. Created in 2013, it is based on a
                    <a href="/collective-impact">collective impact</a> model, an innovative approach to
                    tackling complex social and systemic issues, which requires the coordinated efforts of cross-sector
                    stakeholders.</p>
                <p class="">With the rising incidence of Alzheimer's disease and related dementias in the local community
                    and the complexity of this dementia challenge, a need to create a group of people and organizations that
                    can collectively identify the challenging questions that surround dementia and implement shared solutions
                    that benefit our entire community was identified.</p>
                <p class="">Dementia Network Calgary was created in response to this critical need; embracing an approach
                    that spans the traditional boundaries of sector and discipline, and intentionally cultivates collaboration
                    across multiple sectors. Today, Dementia Network Calgary includes representatives from the following
                    stakeholder groups:</p>

                <ul>
                    <li> Families/care partners;</li>
                    <li> Private and public care facilities;</li>
                    <li> Non-profit service providers; </li>
                    <li> Community organizations; </li>
                    <li> Health services; </li>
                    <li> Post-secondary institutions; and </li>
                    <li> Government. </li>
                </ul>

